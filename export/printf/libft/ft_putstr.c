#include "libft.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		return (write(2, "(null)", 6));
	}
	while (str[i] != '\0')
	{
		write (2, &str[i], 1);
		i++;
	}
	return (i);
}
